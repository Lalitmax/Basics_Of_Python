# create a list using []
a = [3,6,8,2,55,11]

# print the list using print() function
print(a)

# access using index using a[], a[1] , a[3]
print(a[4])

# change the value of list using 
a[0] = 456
print(a)

# also access any data type
b = ["lalit", "pandey", 55, "lalitit"]
print(b)

# list slicing 
friends = ["lalit", "rahul", "pandey", "raushan", 45 , "a", 5]
print(friends[0:4])
